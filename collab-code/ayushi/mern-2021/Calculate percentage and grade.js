var maths = 90
var physics = 70
var chemistry = 80
var english = 70
var hindi = 60

var percentage = ( maths + physics + chemistry + english + hindi) / 5
console.log( percentage)

if(percentage >= 90)
{ 
    console.log(" Grade A")
}
else if(percentage >= 80)
{ 
    console.log(" Grade B")
}
else if(percentage >= 70)
{ 
    console.log(" Grade C")
}
else if(percentage >= 60)
{ 
    console.log(" Grade D")
}
else if(percentage >= 50)
{ 
    console.log(" Grade E")
}
else if(percentage >= 40)
{ 
    console.log(" Grade f")
}
else{
    console.log("NAN")
}


